#define STATUS  OS_STATUS
#define OK      OS_STATUS_OK
#define ERROR   OS_STATUS_ERROR
/**********************************************************************************************************
** Function name:           wtdStart
** Descriptions:            启动 看门狗
                            启动看门狗后, 必须不断的调用 wtdClear() 以避免系统复位, 直到停止 看门狗
                            如果在 【count * 16 * (0x80 + 1) * (1/PCLK)】 秒之内, 如果没有调用 wtdClear(),
                            则系统就会复位. (PCLK = 66M HZ)
** input parameters:        count: 看门狗复位时间计数 [0, 0xFFFF]
** output parameters:       NONE
** Returned value:          OK
** Created by:              WangDongfang
** Created Date:            2011-12-14
**---------------------------------------------------------------------------------------------------------
** Modified by:
** Modified date:
**---------------------------------------------------------------------------------------------------------
**********************************************************************************************************/
STATUS wtdStart (unsigned short count);

/**********************************************************************************************************
** Function name:           wtdStop
** Descriptions:            停止 看门狗
** input parameters:        NONE
** output parameters:       NONE
** Returned value:          OK
** Created by:              WangDongfang
** Created Date:            2011-12-14
**---------------------------------------------------------------------------------------------------------
** Modified by:
** Modified date:
**---------------------------------------------------------------------------------------------------------
**********************************************************************************************************/
STATUS wtdStop ();

/**********************************************************************************************************
** Function name:           wtdClear
** Descriptions:            清空 看门狗
                            在启动看门狗后, 调用此函数以防止系统复位
** input parameters:        NONE
** output parameters:       NONE
** Returned value:          OK
** Created by:              WangDongfang
** Created Date:            2011-12-14
**---------------------------------------------------------------------------------------------------------
** Modified by:
** Modified date:
**---------------------------------------------------------------------------------------------------------
**********************************************************************************************************/
STATUS wtdClear ();

